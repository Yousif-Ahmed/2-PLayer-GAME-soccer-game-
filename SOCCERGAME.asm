main_screen MACRO username_buffer, username, actual_size, name_message, enter_key, error_message, p
LOCAL reEnter, NoError, v1, v2,v3, v4, n1, n2, n3, n4, validated
	;-------------------------------- clear screen
									  mov si,0
	reEnter:                          clear_screen

	;-------------------------------  decoration
	;-------------------------------  cursor
	                                  mov   ah,2
	                                  mov   bh,0
	                                  mov   dl,36
	                                  mov   dh,4
	                                  int   10h

	;-------------------------------- print PLAYER 1
	                                  mov   dx,offset p
	                                  mov   ah,9
	                                  int   21h

	;-------------------------------- print '[]'
	                                  mov   ah,2
	                                  mov   dh,4
	                                  mov   dl,35
	                                  int   10h
	                                  mov   dl,'['
	                                  int   21h


	                                  mov   dh,4
	                                  mov   dl,44
	                                  int   10h
	                                  mov   dl,']'
	                                  int   21h

	;-------------------------------- move cursor to screen center
	                                  mov   ah,2
	                                  mov   bh,0
	                                  mov   dl,29
	                                  mov   dh,10
	                                  int   10h

	;-------------------------------- print name message
	                                  mov   dx,offset name_message
	                                  mov   ah,9
	                                  int   21h

	;-------------------------------- move cursor
	                                  mov   ah,2
	                                  mov   dl,29
	                                  mov   dh,14
	                                  int   10h

	;-------------------------------- print enter message
	                                  mov   dx,offset enter_key
	                                  mov   ah,9
	                                  int   21h

	;-------------------------------- print error message if any
	                                  cmp   si,-1
	                                  jnz   NoError
	                                  mov   bh,0
	                                  mov   ah,2
	                                  mov   dl,0
	                                  mov   dh,24
	                                  int   10h

	                                  mov   dx,offset error_message
	                                  mov   ah,9
	                                  int   21h

	;-------------------------------- read name
	NoError:                          
	                                  mov   ah,2
	                                  mov   dl,33
	                                  mov   dh,12
	                                  int   10h

	                                  mov   ah,0ah
	                                  mov   dx,offset username_buffer
	                                  int   21h

	;-------------------------------- validate name
	                                  cmp   username, 'a'
	                                  jae   v1
	                                  mov   bl,0
	                                  jmp   n1
	v1:                               mov   bl,1

	n1:                               cmp   username,'z'
	                                  jbe   v2
	                                  mov   bh,0
	                                  jmp   n2
	v2:                               mov   bh,1

	n2:                               and   bh,bl

	                                  cmp   username, 'A'
	                                  jae   v3
	                                  mov   cl,0
	                                  jmp   n3
	v3:                               mov   cl,1

	n3:                               cmp   username,'Z'
	                                  jbe   v4
	                                  mov   ch,0
	                                  jmp   n4
	v4:                               mov   ch,1

	n4:                               and   ch,cl

	                                  or    bh,ch
	                                  jnz   validated

	;-------------------------------- indicate that the name is incorrect
	                                  mov   si,-1                                                    
	                                  jmp   reEnter


	;-------------------------------- put $ at the end of the name
	validated:                        mov   bl,actual_size
	                                  mov   bh,0
	                                  mov   username[bx], '$'
ENDM  main_screen


